Tetrimino::Tetrimino():
    mTetriminoId(0), mRotationId(0), mX(0), mY(0)
{
}

void Tetrimino::setValues(int tetriminoId, int rotationId, int x, int y)
{
    mTetriminoId = tetriminoId;
    mRotationId = rotationId;
    mX = x;
    mY = y;
}

int Tetrimino::GetType(int pX, int pY, int tetriminoId, int rotationId)
{
	return mTetrimino[tetriminoId][rotationId][pX][pY];
}

int Tetrimino::GetInitOffsetX(int tetriminoId, int rotationId)
{
    return mTetriminoInitOffset[tetriminoId][rotationId][0];
}

int Tetrimino::GetInitOffsetY(int tetriminoId, int rotationId)
{
    return mTetriminoInitOffset[tetriminoId][rotationId][1];
}